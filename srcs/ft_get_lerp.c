#include "fdf.h"

int32_t	ft_get_lerp(double z1, double z2, double z)
{
	return ((int32_t)(((COLOR_MAX_Z * (z2 - z)) + (COLOR_MIN_Z
			* (z - z1))) / (z2 - z1)));
}
